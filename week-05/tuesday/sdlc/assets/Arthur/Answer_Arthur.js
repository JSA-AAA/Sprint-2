// 2. Waterfall Model vs Agile Method?
//瀑布：花费更多的时间在项目的前期，一旦需求改变，或者某一阶段发生问题或者错误代价巨大

//Agile： 敏捷开发却放弃项目计划阶段，取而代之的事开发人员通过以周为单位的短期冲刺来针对小型的模块开发，
// 这样需求的变更往往影响是最小的。而且客户是参与整个开发周期的，
// 通过与开发人员的频繁交流来确保项目的正确性。
// 然而相对瀑布模型明确的时间线和里程碑，敏捷开发最大的挑战是难以计算的时间线与项目预算，
// 例如Scrum的燃尽图就是解决这一问题的一个方案。
